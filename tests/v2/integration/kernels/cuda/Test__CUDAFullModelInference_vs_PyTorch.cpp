/**
 * @file Test__CUDAFullModelInference_vs_PyTorch.cpp
 * @brief Integration: CUDA Pipeline vs PyTorch Reference
 *
 * @category integration/cuda/parity
 * @tested   CUDA inference pipeline (GraphOrchestrator)
 * @reference PyTorch Qwen2 implementation (ground truth)
 *
 * @author David Sanftenberg
 * @date 2026-01-10
 *
 * Validates CUDA inference correctness by comparing against PyTorch ground truth.
 * Uses the EXACT SAME pass/fail thresholds as the CPU vs PyTorch parity tests.
 *
 * This test supersedes the CUDA vs CPU parity tests for determining whether
 * CUDA inference is "correct enough". Both CPU and CUDA use lossy INT8
 * quantization, so comparing against the FP32 PyTorch oracle gives a true
 * measure of inference quality.
 *
 * Test Strategy:
 * - Load PyTorch snapshots generated by generate_qwen_pipeline_snapshots.py
 * - Run inference with same input tokens via IInferenceRunner (CUDA path)
 * - Compare intermediate activations stage-by-stage
 *
 * Pass/Fail Criteria (same as CPU vs PyTorch):
 * - First 4 layers: At least 3 must have cosine similarity >= 0.999
 * - LM_HEAD: KL divergence < 0.15
 * - Decode steps: Average cosine >= 0.99, Top-1 accuracy >= 60%
 *
 * Requirements:
 * - PyTorch snapshots: run `python3 python/reference/generate_qwen_pipeline_snapshots.py`
 * - Qwen 2.5 0.5B model: models/qwen2.5-0.5b-instruct-q4_0.gguf
 * - CUDA GPU available
 */

