/// Cosine similarity threshold for layer-by-layer comparison (AVERAGE cosine)
/// CUDA uses per-row symmetric INT8 which accumulates error faster than CPU's
/// per-block Q8_1. We use 0.99 avg cosine for early layers - deep layer
/// divergence is acceptable as long as final output quality is good.
static constexpr float COSINE_THRESHOLD = 0.99f;

/// KL divergence threshold for logits
static constexpr float KL_THRESHOLD = 0.15f;

